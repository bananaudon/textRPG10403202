package textRPG10403202;
public class eventE {
    int probability;
    int eventType;
    int eventPower;

    eventE() {
        probability = 0;
        eventType = 0;
        eventPower = 0;
    }

    eventE(int P, int ET, int EP) {
        probability = P;
        eventType = ET;
        eventPower = EP;
    }
}